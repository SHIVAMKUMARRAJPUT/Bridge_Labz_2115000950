// Objects store key-value pairs.

// Object declaration
let person = {
    name: "Shivam",
    age: 22,
    city: "Bulandshahr"
};

// Print statements
console.log("Name:", person.name); // Output: Shivam
console.log("Age:", person.age);   // Output: 22
console.log("City:", person.city); // Output: Bulandshahr
